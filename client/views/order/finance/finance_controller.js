this.OrderFinanceController = RouteController.extend({
	template: "OrderFinance",
	

	yieldTemplates: {
		/*YIELD_TEMPLATES*/
	},

	onBeforeAction: function() {
		this.next();
	},

	action: function() {
		if(this.isReady()) { this.render(); } else { this.render("loading"); }
		/*ACTION_FUNCTION*/
	},

	isReady: function() {
		

		var subs = [
			Meteor.subscribe("invoice_list")
		];
		var ready = true;
		_.each(subs, function(sub) {
			if(!sub.ready())
				ready = false;
		});
		return ready;
	},

	data: function() {
		

		return {
			params: this.params || {},
			invoice_list: Invoices.find({}, {sort:{invoiceNumber:-1}})
		};
		/*DATA_FUNCTION*/
	},

	onAfterAction: function() {
		
	}
});